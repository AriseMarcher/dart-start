void main() {
  // 声明变量
  var age = 18;

  print(age);

  String name = 'xiaoming';

  print(name);

  dynamic myname = 20; // 动态的变量
  print(myname);

  var defaultName;

  print(defaultName);

  var Name = '123';
  print(Name);

  // 常量
  const n1 = 1;
  // n1 = 6; // 常量一旦声明 不能再改动
  print(n1);

  final n2 = 2;
  print(n2);
}
